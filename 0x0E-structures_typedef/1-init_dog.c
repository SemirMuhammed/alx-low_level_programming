#include "dog.h"

/**
 * init_dog - Initializes all the stuct dog values
 * @d: the value of stuct dog
 * @name: the dog's name
 * @age: the dog's age
 * @owner: the dog's owner
 *
 * Return: Void
 */
void init_dog(struct dog *d, char *name, float age, char *owner)
{
	struct d;

	d->name = name;
	d->age = age;
	d->owner = owner;
}
